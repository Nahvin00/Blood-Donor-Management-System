package Model;

public class Event {

	private String event_NAME;
	private String event_DATE;
	private String event_VENUE;
	private String event_TIME;
	private String event_REFERID;
	private String event_CONTACT;
	public String getEvent_NAME() {
		return event_NAME;
	}
	public void setEvent_NAME(String event_NAME) {
		this.event_NAME = event_NAME;
	}
	public String getEvent_DATE() {
		return event_DATE;
	}
	public void setEvent_DATE(String event_DATE) {
		this.event_DATE = event_DATE;
	}
	public String getEvent_VENUE() {
		return event_VENUE;
	}
	public void setEvent_VENUE(String event_VENUE) {
		this.event_VENUE = event_VENUE;
	}
	public String getEvent_TIME() {
		return event_TIME;
	}
	public void setEvent_TIME(String event_TIME) {
		this.event_TIME = event_TIME;
	}
	public String getEvent_REFERID() {
		return event_REFERID;
	}
	public void setEvent_REFERID(String event_REFERID) {
		this.event_REFERID = event_REFERID;
	}
	public String getEvent_CONTACT() {
		return event_CONTACT;
	}
	public void setEvent_CONTACT(String event_CONTACT) {
		this.event_CONTACT = event_CONTACT;
	}

	
}
